package Basic;

public class math {

	public static void main(String[] args) {
		//제곱 구하기
		//2의 3승
		double a = Math.pow(2,3); //math.pow는 double타입 반환
		System.out.println(a); // 결과값 8.0
		
		//제곱근 구하기
		//4의 제곱근
		double b = Math.sqrt(4); //math.sqrt는 double타입 반환
		System.out.println(b); //결과값 2.0

	}

}
