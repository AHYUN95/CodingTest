package JavaSyntax;

public class TernaryOperator {

	public static void main(String[] args) {
		// if-else표현
		int a;
		if (5<4) {
			a=50;
		}else {
			a=40;}
		
		System.out.println(a); // 결과 = 40
		
		//삼항연산자 표현
		// 조건식?(참일때의 값): (거짓일떄의 값)ㄴ
		int b = (5<4)? 50 :40;
		System.out.println(b); // 결과 = 40
		
		//3개 비교
		//(a>b ? a:b)>c ? (a>b ? a:b):c
	
	
	}
	

	}


